//Write program to perform all the arithmetic operations given in the table
public class ArithmeticOperations {

	public static void main(String[] args) {
		//creating 2 integers and performing operations
		int a=100,b=50;
		        //1.Addition operation
				System.out.println("Addition: "+(a+b));
				//2.Subtraction operation
				System.out.println("Subtraction: "+(a-b));
				//3.Multiplication operation
				System.out.println("Mutliplication: "+(a*b));
				//4.Division operation
				System.out.println("Division: "+(a/b));
				//5.Increment operator
				System.out.println("Increment: "+(a++ + ++b));
				//6.Decrement operator
				System.out.println("Decrement: "+(a-- + --b ));
				
		
	}

}
